    <h1>HTML标题</h1>
    <p>标题(heading)通过&lt;h1&gt; ~ &lt;h6&gt;标签进行定义</p>

    <div class="code">
        <h1>&lt;h1&gt;这是h1&lt;/h1&gt;</h1>
        <h2>&lt;h2&gt;这是h2&lt;/h2&gt;</h2>
        <h3>&lt;h3&gt;这是h3&lt;/h3&gt;</h3>
        <h4>&lt;h4&gt;这是h4&lt;/h4&gt;</h4>
        <h5>&lt;h5&gt;这是h5&lt;/h5&gt;</h5>
        <h6>&lt;h6&gt;这是h6&lt;/h6&gt;</h6>
    </div>
    <p >
        <b>注</b> 浏览器会自动在标题的前后添加空行
    </p>
    <ol>
        <li>请确保将HTML标题标签只用于标题，不要仅仅是为了生成粗体或大号的文本而使用标题</li>
        <li>搜索引擎使用标题来快速浏览您的网页，所以用标题来呈现文档结构是很重要的</li>
        <li>应该将h1用作标题（最重要），其次是h2,依次...</li>
    </ol>
